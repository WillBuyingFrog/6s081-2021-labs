uint64
count_free_memory(void)
{
  uint64 counter;
  struct run *init_run;
  init_run = kmem.freelist;
  if(init_run){
    // 如果freelist存在，那么从freelist开始不断遍历，直到回到freelist
    struct run *r;
    r = init_run;
    // 至少有一页的内存
    counter = 1;
    while(1){
      if((uint64)r->next >= PHYSTOP || (char*)r->next < end){
        break;
      }
      r = r->next;
      counter = counter + 1;
    }
    counter = counter * PGSIZE;
    return counter;
  }else{
    return 0;
  }
}
